"""Contract tests for the `version_plan` MCP tool."""

from uuid import uuid4

import pytest
from fastapi.testclient import TestClient

from cora.api.main import create_app
from tests.contract._mcp_helpers import open_session, parse_sse_data


def _setup_plan(client: TestClient) -> str:
    cap_id = client.post("/families", json={"name": "FlyMotion", "affordances": []}).json()[
        "family_id"
    ]
    method_id = client.post(
        "/methods", json={"name": "Test Method", "needed_families": [cap_id]}
    ).json()["method_id"]
    practice_id = client.post(
        "/practices",
        json={"name": "Test Practice", "method_id": method_id, "site_id": str(uuid4())},
    ).json()["practice_id"]
    asset_id = client.post(
        "/assets",
        json={"name": "TestAsset", "level": "Enterprise", "parent_id": None},
    ).json()["asset_id"]
    client.post(f"/assets/{asset_id}/add_family", json={"family_id": cap_id})
    plan_id = client.post(
        "/plans",
        json={"name": "32-ID FlyScan", "practice_id": practice_id, "asset_ids": [asset_id]},
    ).json()["plan_id"]
    return plan_id


@pytest.mark.contract
def test_mcp_lists_version_plan_tool() -> None:
    with TestClient(create_app()) as client:
        headers = open_session(client)
        response = client.post(
            "/mcp",
            json={"jsonrpc": "2.0", "id": 99, "method": "tools/list"},
            headers=headers,
        )
    body = parse_sse_data(response.text)
    tool_names = [t["name"] for t in body["result"]["tools"]]
    assert "version_plan" in tool_names


@pytest.mark.contract
def test_mcp_version_plan_tool_succeeds_on_happy_path() -> None:
    with TestClient(create_app()) as client:
        plan_id = _setup_plan(client)
        headers = open_session(client)
        response = client.post(
            "/mcp",
            json={
                "jsonrpc": "2.0",
                "id": 4,
                "method": "tools/call",
                "params": {
                    "name": "version_plan",
                    "arguments": {"plan_id": plan_id, "version_tag": "v2"},
                },
            },
            headers=headers,
        )
    body = parse_sse_data(response.text)
    assert body["result"]["isError"] is False


@pytest.mark.contract
def test_mcp_version_plan_tool_returns_iserror_for_unknown_plan() -> None:
    with TestClient(create_app()) as client:
        headers = open_session(client)
        response = client.post(
            "/mcp",
            json={
                "jsonrpc": "2.0",
                "id": 5,
                "method": "tools/call",
                "params": {
                    "name": "version_plan",
                    "arguments": {"plan_id": str(uuid4()), "version_tag": "v1"},
                },
            },
            headers=headers,
        )
    body = parse_sse_data(response.text)
    assert body["result"]["isError"] is True
    assert "not found" in body["result"]["content"][0]["text"].lower()


@pytest.mark.contract
def test_mcp_version_plan_tool_returns_iserror_when_deprecated() -> None:
    with TestClient(create_app()) as client:
        plan_id = _setup_plan(client)
        client.post(f"/plans/{plan_id}/deprecate")
        headers = open_session(client)
        response = client.post(
            "/mcp",
            json={
                "jsonrpc": "2.0",
                "id": 6,
                "method": "tools/call",
                "params": {
                    "name": "version_plan",
                    "arguments": {"plan_id": plan_id, "version_tag": "v2"},
                },
            },
            headers=headers,
        )
    body = parse_sse_data(response.text)
    assert body["result"]["isError"] is True
