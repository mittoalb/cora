def _rel(path: Path) -> str:
    """Path relative to apps/api/, matching the allowlist's keying."""
    s = str(path)
    idx = s.rfind(_API_ROOT_MARKER)
    return s[idx + len(_API_ROOT_MARKER) :] if idx != -1 else s


@pytest.mark.architecture
def test_no_em_dashes_in_tracked_python_files() -> None:
    """Every tracked .py outside the allowlist is em-dash-free.

    The allowlist is the snapshot of files carrying em-dashes when the
    rule grew teeth. New files (and previously-clean files that grow
    em-dashes) fail here. Scrub a file by removing every em-dash in it,
    then dropping its entry from ``EMDASH_ALLOWLIST``; the meta-test
    will keep that drop honest.
    """
    violators: list[Path] = []
    for path in sorted(tracked_python_files() | tracked_test_files()):
        if _rel(path) in EMDASH_ALLOWLIST:
            continue
        text = path.read_text(encoding="utf-8")
        if _EM_DASH in text:
            violators.append(path)

    if not violators:
        return

    msg_lines = [
        f"Found em-dash in {len(violators)} tracked .py file(s) not in EMDASH_ALLOWLIST.",
        "Substitute commas, colons, or rephrase. See CLAUDE.md hard rules.",
        "",
    ]
    for path in violators[:20]:
        msg_lines.append(f"  {_rel(path)}")
    if len(violators) > 20:
        msg_lines.append(f"  ... and {len(violators) - 20} more")
    pytest.fail("\n".join(msg_lines))


@pytest.mark.architecture
def test_em_dash_allowlist_entries_actually_contain_em_dashes() -> None:
    """Every allowlist entry must still carry at least one em-dash.

    Catches stale entries (the file was scrubbed but the allowlist was
    not updated). Keeps the ratchet trustworthy: an entry that no
    longer points at a violation must be removed so the allowlist only
    ever shrinks.
    """
    tracked_by_rel = {_rel(p): p for p in tracked_python_files() | tracked_test_files()}
    stale: list[str] = []
    missing: list[str] = []
    for rel in sorted(EMDASH_ALLOWLIST):
        path = tracked_by_rel.get(rel)
        if path is None:
            missing.append(rel)
            continue
        text = path.read_text(encoding="utf-8")
        if _EM_DASH not in text:
            stale.append(rel)

    msg_lines: list[str] = []
    if missing:
        msg_lines.append(
            f"{len(missing)} EMDASH_ALLOWLIST entries reference files that no "
            "longer exist in the tracked .py set; remove them:"
        )
        msg_lines.extend(f"  {rel}" for rel in missing[:10])
        if len(missing) > 10:
            msg_lines.append(f"  ... and {len(missing) - 10} more")
    if stale:
        msg_lines.append(
            f"{len(stale)} EMDASH_ALLOWLIST entries no longer contain an em-dash "
            "(scrubbed without updating the allowlist); remove them:"
        )
        msg_lines.extend(f"  {rel}" for rel in stale[:10])
        if len(stale) > 10:
            msg_lines.append(f"  ... and {len(stale) - 10} more")
    if msg_lines:
        pytest.fail("\n".join(msg_lines))
